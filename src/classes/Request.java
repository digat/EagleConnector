package classes;

/**
 *
 * @author Tareq
 */
public class Request {
    private final int id;
    private final String message;

    public Request(int id, String message) {
        this.id = id;
        this.message = message;
    }

    public int getId() {
        return id;
    }

    public String getMessage() {
        return message;
    }
    
}